const DICTS: Record<UiLang, Dict> = { pt, ht, en, es, fr };

/* -------------------- Contexto -------------------- */

type I18nValue = {
  lang: UiLang;
  setLang: (l: UiLang) => void;
  fontScale: FontScale;
  setFontScale: (s: FontScale) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<UiLang>("pt");
  const [fontScale, setFontScaleState] = useState<FontScale>("md");
  const [ready, setReady] = useState(false);

  useEffect(() => {
    const savedLang = localStorage.getItem(LS_UI_LANG) as UiLang | null;
    const initialLang =
      savedLang && DICTS[savedLang] ? savedLang : detectDeviceLang();
    setLangState(initialLang);

    const savedScale = localStorage.getItem(LS_FONT_SCALE) as FontScale | null;
    const initialScale =
      savedScale && FONT_SCALES.includes(savedScale) ? savedScale : "md";
    setFontScaleState(initialScale);
    setReady(true);
  }, []);

  // Aplica idioma e tamanho de fonte ao <html>
  useEffect(() => {
    if (typeof document === "undefined") return;
    document.documentElement.lang = lang;
  }, [lang]);

  useEffect(() => {
    if (typeof document === "undefined") return;
    document.documentElement.setAttribute("data-font-scale", fontScale);
  }, [fontScale]);

  const setLang = useCallback((l: UiLang) => {
    setLangState(l);
    try {
      localStorage.setItem(LS_UI_LANG, l);
    } catch {}
  }, []);

  const setFontScale = useCallback((s: FontScale) => {
    setFontScaleState(s);
    try {
      localStorage.setItem(LS_FONT_SCALE, s);
    } catch {}
  }, []);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const dict = DICTS[lang] || pt;
      let str = dict[key] ?? pt[key] ?? key;
      if (vars) {
        for (const [k, v] of Object.entries(vars)) {
          str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
        }
      }
      return str;
    },
    [lang],
  );

  // Evita flash do idioma errado antes da hidratação do localStorage
  const value: I18nValue = { lang, setLang, fontScale, setFontScale, t };

  return (
    <I18nContext.Provider value={value}>
      {ready ? children : <div style={{ visibility: "hidden" }}>{children}</div>}
    </I18nContext.Provider>
  );
}

export function useI18n(): I18nValue {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error("useI18n deve ser usado dentro de <I18nProvider>");
  }
  return ctx;
}
